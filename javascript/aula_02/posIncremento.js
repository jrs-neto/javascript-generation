// Operadores de Incremento e Decremento
/*
++x Pré-Incremento
--x Pré-Decremento
x++ Pós-Incremento
x-- Pós-Decremento
*/

let numero = 1;
console.log(`Valor atual: ${numero}`);

console.log(`Valor da váriavel número - Pós incrementada ${numero++}`);

console.log(`Valor atualizado da váriavel número ${numero}`);